# Estructura de bucle For

# Sintaxis
# for value in sequence:
#   instrucción, que es lo que debe hacer mientras se cumple el bucle

# Ejemplo:

# Listado de frutas
frutas = ["manzana", "mandarina", "arándanos", "fresas", "maracuyá"]

for fruta in frutas:
    print(fruta)

# listado de carros
carros = ["toyota", "nissan", "chevrolec", "audi", "kia"]

for carro in carros:
    print(carro)
    
