# Obtener el promedio de un estudiante a partir de 3 notas

# Inicio: Definir variables de las notas
nota_t1 = 16
nota_t2 = 10
nota_ef = 13

# Proceso: Definir el proceso de cálculo del promedio de notas
promedio_notas = (nota_t1 + nota_t2 + nota_ef) / 3

# Salida: Mostrar el resultado del promedio
print(f"promedio de robert : {promedio_notas}")

